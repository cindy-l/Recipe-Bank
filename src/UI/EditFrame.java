    public EditFrame(MainScreen m, String key){
        initComponents();
        ms = m;
        recipeName = key;
        EditName.setText(recipeName);
        PreparedStatement ps;
        try{
            ps = ms.con.prepareStatement("SELECT info FROM Recipe WHERE id = ?");
            ps.setString(1, recipeName);
            ResultSet rs = ps.executeQuery();
            while (rs.next())
                EditText.setText(rs.getString("info"));
        }catch(SQLException e){
            System.out.println("Message is: " + e.getMessage());
        }
                
    }
