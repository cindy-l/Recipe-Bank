    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed

        if (ms.mod.contains(EditName.getText()) && !(EditName.getText().equals(recipeName))){
            JOptionPane.showMessageDialog(rootPane, "Recipe name already taken.");
        }else{
            ms.deleteBranch(recipeName);
            ms.addRecipe(EditName.getText(), EditText.getText());
            dispose();
        }
    }//GEN-LAST:event_saveButtonActionPerformed
    
    
